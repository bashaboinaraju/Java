class primitives {
  public static void main(String[] args) {
    	
    boolean flag = true;
    byte range;
    short temperature;
    int r = 10000;
    float number = -42.3f;
    double num1 = -42.3;
    char letter1 = '5';
    String myString = "Java Programming";
    System.out.println(flag);    // prints true
    range = 124;
    System.out.println(range);    // prints 124
    temperature = -200;
    System.out.println(temperature);  // prints -200
    System.out.println(r);  // print -4250000
    System.out.println(number);  // prints -42.3
    System.out.println(num1);  // prints -42.3
    System.out.println(letter1);  // prints A
    System.out.println(myString); 
  }
}